// Input: an array of integers
// Output: a sorted array of integers
// Constraints: none
// Edge cases: none

function selectionSort(arr) {
  // iterate over array
    // store current index as min
      // iterate over rest of array
        // if smaller element is found
          // assign it as min
      // if min changed
        // swap current element with element where min is
  for (let i = 0; i < arr.length; i++) {
    let min = i;
    for (let j = i + 1; j < arr.length; j++) {
      if (arr[j] < arr[min]) {
        min = j;
      }
    }
    if (min !== i) {
      let temp = arr[i];
      arr[i] = arr[min];
      arr[min] = temp;
    }
  }

  return arr;
}

